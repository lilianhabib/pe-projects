<h1 class='faq-title'>Frequently Asked Questions</h1>


<ul class='list-faq'> 
	<li class="question">
		<h2>Am I able to care for a cat?</h2>
		<p>This may seem like a no-brainer, but it’s a question whose answer many people don’t listen to. Go through your day from the moment you wake up to the moment you go to sleep. Can your life and your home accommodate a new family member?</p>
	</li>


	<li class="question">
		<h2>Can you afford the day to day costs of pet ownership like food and supplements?</h2>

		<p>Can you afford an unexpected expense like an illness or accident? If you’re unsure about any of these things, you may not be ready yet to be a pet parent.</p>
	</li>

	<li class="question"> 
		<h2>Is my whole family on the same page with regard to caregiving responsibilities?</h2>

		<p>If kids or spouses will have different roles in caregiving, make the roles clear from the get go. Consider making a game of it. Write up a contract of pet duties, and have the whole family sign it. Consider a rotating chart. If children are involved, include affection and playtime on the Kitty To Do Chart, to be sure your new family member is getting the attention and stimulation that’s integral to feline contentment.</p>
	</li>

<h3 class='subtitle-faq'>Questions to Ask the Shelter</h3> 

	<li class="question">
		<h2>Do you know anything about the cat’s history?</h2>
		<p>Sometimes, a “surrendered” cat will come with a bit of history. There are often clues as to the cat’s temperament. For example, were they a rambunxious member of a multi-pet household? Or were they a staid only cat who likes to rule the roost?</p>
	
	</li>

	<li class="question"> 
		<h2>May I spent any one-on-one time with the cat?</h2>

		<p>Most shelters will allow prospective adopters to go into a room or other closed off area to interact with their kitties of choice. If this isn’t offered, request it. Try little games that might give you a sense of the cat’s personality and try to connect with them. If they’re a bit used to your scent and your voice, they’ll feel safer when you bring them home.</p>
	</li>

	<li class="question"> 
		<h2>Do you have a medical history for the cat?</h2>
		<p>Every shelter should do an intake exam on all animals. Any known health issues should be disclosed. If the cat has a long term or progressive illness, do your research. You don’t want to take on more than you can handle.</p>
	</li>

	<li class="question">
		<h2>Is s/he already spayed or neutered?</h2>
		<p>If your cat will be allowed to roam outdoors, “fixing” your pet is the responsible thing to do. Many shelters offer spay/neuter services. If not, schedule an appointment with your veterinarian as soon as possible.</p>
	</li>

	<li class="question">
		<h2>Can you recommend a veterinarian?</h2>

		<p>If you don’t already have a veterinarian, seek one out before you adopt. Make an appointment as close to the date of adoption as possible, and bring kitty in for a check up.</p>
	</li>

	<li class="question"> 
		<h2>Am I sure I’ve chosen the right animal for me and my family?</h2>

		<p>Have you visited several times? Are you sure you’re adopting because you really click with your chosen cat, and not because you’re in a hurry for some reason? Most adoptive pet parents will tell you: you’ll know it when it happens! Wait for the right connection, and use your intuition.</p>
	</li>

	<li class="question">
		<h2>Is my physical space safe and ready for a pet?</h2>

		<p>Be sure to move all your breakables to a safe area or closed shelf. If there are areas you won’t want your cat to venture -- like a stove top or dining table -- consider covering those surfaces in face-up tape or aluminum foil for the first few days. Your cat will dislike the feel and stay away from these surfaces. Keep an eye on loose curtain pulls (because of the danger of strangulation) and electrical cords (which can cause electrocution, if chewed). Your cat may appreciate a small, quiet room or space to themselves for the first few days, as they get used to their new environment.</p>
	</li>
</ul>